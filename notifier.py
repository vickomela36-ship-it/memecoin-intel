_PAIR_URL_ROW = """
    <tr style="background:#f0fdf4">
      <td style="padding:8px;font-weight:bold">Chart</td>
      <td style="padding:8px"><a href="{url}">{url}</a></td>
    </tr>
"""


def _build_email(sig: TokenSignal) -> MIMEMultipart:
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S")
    pair_url_row = _PAIR_URL_ROW.format(url=sig.pair_url) if sig.pair_url else ""
    html = _HTML_TEMPLATE.format(
        symbol=sig.symbol,
        chain=sig.chain,
        signal=sig.signal.upper(),
        price_usd=sig.price_usd,
        h1=sig.price_change_1h,
        h6=sig.price_change_6h,
        h24=sig.price_change_24h,
        volume=sig.volume_24h_usd,
        mcap=sig.market_cap_usd,
        liquidity=sig.liquidity_usd,
        score=sig.score,
        reasons="; ".join(sig.reasons),
        address=sig.address,
        pair_url_row=pair_url_row,
        timestamp=timestamp,
    )
    plain = (
        f"BUY SIGNAL: {sig.symbol} ({sig.chain})\n"
        f"Price: ${sig.price_usd:.8f}\n"
        f"24h: {sig.price_change_24h:+.1f}%  Volume: ${sig.volume_24h_usd:,.0f}\n"
        f"Score: {sig.score}/6  Reasons: {'; '.join(sig.reasons)}\n"
        f"Contract: {sig.address}\n"
        f"Chart: {sig.pair_url}\n"
        f"\nThis is not financial advice."
    )
    msg = MIMEMultipart("alternative")
    msg["Subject"] = f"🚀 Buy Signal: {sig.symbol} ({sig.price_change_24h:+.1f}% 24h)"
    msg["From"] = config.GMAIL_USER
    msg["To"] = config.ALERT_EMAIL_TO
    msg.attach(MIMEText(plain, "plain"))
    msg.attach(MIMEText(html, "html"))
    return msg


def send_email(sig: TokenSignal) -> bool:
    """Send a buy-signal email via Gmail SMTP. Returns True on success."""
    if not config.GMAIL_USER or not config.GMAIL_APP_PASSWORD:
        logger.warning("Email credentials not configured — skipping email for %s", sig.symbol)
        return False
    try:
        msg = _build_email(sig)
        ctx = ssl.create_default_context()
        with smtplib.SMTP_SSL("smtp.gmail.com", 465, context=ctx) as server:
            server.login(config.GMAIL_USER, config.GMAIL_APP_PASSWORD)
            server.sendmail(config.GMAIL_USER, config.ALERT_EMAIL_TO, msg.as_string())
        logger.info("Email sent for %s to %s", sig.symbol, config.ALERT_EMAIL_TO)
        return True
    except Exception as exc:
        logger.error("Failed to send email for %s: %s", sig.symbol, exc)
        return False


# ── Notion ─────────────────────────────────────────────────────────────────────

_NOTION_PAGES_URL = "https://api.notion.com/v1/pages"
_NOTION_HEADERS = {
    "Authorization": f"Bearer {config.NOTION_TOKEN}",
    "Content-Type": "application/json",
    "Notion-Version": "2022-06-28",
}


def _notion_headers() -> dict:
    return {
        "Authorization": f"Bearer {config.NOTION_TOKEN}",
        "Content-Type": "application/json",
        "Notion-Version": "2022-06-28",
    }


def log_to_notion(sig: TokenSignal, email_sent: bool) -> bool:
    """Append a row to the Memecoin Buy Signals Notion database."""
    if not config.NOTION_TOKEN:
        logger.warning("NOTION_TOKEN not configured — skipping Notion log for %s", sig.symbol)
        return False

    now_iso = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S.000Z")
    payload: dict = {
        "parent": {"database_id": config.NOTION_DATABASE_ID},
        "properties": {
            "Token": {
                "title": [{"type": "text", "text": {"content": sig.symbol}}]
            },
            "Contract Address": {
                "rich_text": [{"type": "text", "text": {"content": sig.address}}]
            },
            "Signal": {
                "select": {"name": sig.signal}
            },
            "Price USD": {"number": sig.price_usd},
            "Price Change 24h %": {"number": sig.price_change_24h},
            "Volume 24h USD": {"number": sig.volume_24h_usd},
            "Market Cap USD": {"number": sig.market_cap_usd},
            "Liquidity USD": {"number": sig.liquidity_usd},
            "Reasons": {
                "rich_text": [{"type": "text", "text": {"content": "; ".join(sig.reasons)}}]
            },
            "Chain": {
                "select": {"name": sig.chain if sig.chain in {"solana", "ethereum", "bsc", "base"} else "solana"}
            },
            "Timestamp": {
                "date": {"start": now_iso, "time_zone": "UTC"}
            },
            "Email Sent": {"checkbox": email_sent},
        },
    }

    try:
        resp = requests.post(
            _NOTION_PAGES_URL,
            headers=_notion_headers(),
            json=payload,
            timeout=15,
        )
        resp.raise_for_status()
        logger.info("Logged %s to Notion (signal=%s)", sig.symbol, sig.signal)
        return True
    except Exception as exc:
        logger.error("Failed to log %s to Notion: %s", sig.symbol, exc)
        return False


# ── Orchestration ──────────────────────────────────────────────────────────────

def notify_buy_signal(sig: TokenSignal) -> None:
    """Send email + log to Notion for a buy-now signal."""
    email_sent = send_email(sig)
    log_to_notion(sig, email_sent)
