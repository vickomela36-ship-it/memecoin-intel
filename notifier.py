# ---------------------------------------------------------------------------

def log_to_notion(signal: dict, email_sent: bool) -> bool:
    """Append one buy-now signal row to the Notion database. Returns True on success."""
    now = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S+00:00")
    payload = {
        "parent": {"database_id": NOTION_DATABASE_ID},
        "properties": {
            "Token Name":     {"title":     [{"text": {"content": signal["token_name"]}}]},
            "Symbol":         {"rich_text": [{"text": {"content": signal["symbol"]}}]},
            "Signal":         {"select":    {"name": "buy now"}},
            "Price USD":      {"rich_text": [{"text": {"content": str(signal["price_usd"])}}]},
            "Timestamp":      {"date":      {"start": now}},
            "24h Change %":   {"number":    signal["price_change_24h"]},
            "Volume 24h USD": {"number":    signal["volume_24h_usd"]},
            "Liquidity USD":  {"number":    signal["liquidity_usd"]},
            "Vol/Liq Ratio":  {"number":    signal["vol_liq_ratio"]},
            "Pair Address":   {"rich_text": [{"text": {"content": signal["pair_address"]}}]},
            "DexScreener URL":{"url":        signal["dexscreener_url"] or None},
            "Email Sent":     {"checkbox":   email_sent},
        },
    }
    resp = requests.post(_NOTION_URL, headers=_NOTION_HEADERS, json=payload, timeout=15)
    if resp.status_code != 200:
        log.error("Notion log failed (%s): %s", resp.status_code, resp.text[:200])
        return False
    return True
