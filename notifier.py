import smtplib
import os
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText
from datetime import datetime, timezone


def send_buy_signal_email(coins: list, to_email: str) -> None:
    gmail_user = os.environ["GMAIL_USER"]
    gmail_password = os.environ["GMAIL_APP_PASSWORD"]

    subject = f"Memecoin Buy Signal — {len(coins)} coin(s) flagged {datetime.now(timezone.utc).strftime('%Y-%m-%d')}"

    rows = "".join(
        f"<tr>"
        f"<td><b>{c.name}</b> ({c.symbol})</td>"
        f"<td>${c.price_usd:.6g}</td>"
        f"<td style='color:{'green' if c.change_24h >= 0 else 'red'}'>{c.change_24h:+.2f}%</td>"
        f"<td>${c.volume_24h:,.0f}</td>"
        f"<td>${c.market_cap:,.0f}</td>"
        f"</tr>"
        for c in coins
    )

    html = f"""
    <html><body style="font-family:Arial,sans-serif">
      <h2 style="color:#2e7d32">Buy Now Signal Detected</h2>
      <p>The following memecoins triggered a <strong>buy now</strong> signal:</p>
      <table border="1" cellpadding="8" cellspacing="0" style="border-collapse:collapse">
        <tr style="background:#f5f5f5">
          <th>Coin</th><th>Price (USD)</th><th>24h Change</th>
          <th>Volume (24h)</th><th>Market Cap</th>
        </tr>
        {rows}
      </table>
      <br>
      <p style="color:#888;font-size:12px">
        Generated by memecoin-intel &middot;
        {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}
      </p>
    </body></html>
    """

    msg = MIMEMultipart("alternative")
    msg["Subject"] = subject
    msg["From"] = gmail_user
    msg["To"] = to_email
    msg.attach(MIMEText(html, "html"))

    with smtplib.SMTP_SSL("smtp.gmail.com", 465) as server:
        server.login(gmail_user, gmail_password)
        server.sendmail(gmail_user, to_email, msg.as_string())

    print(f"Email sent to {to_email} ({len(coins)} signal(s))")
