import smtplib
import logging
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText
from datetime import datetime, timezone

import requests

from config import (
    GMAIL_SENDER,
    GMAIL_APP_PASSWORD,
    GMAIL_RECIPIENT,
    NOTION_TOKEN,
    NOTION_DATABASE_ID,
)

log = logging.getLogger(__name__)

_NOTION_URL = "https://api.notion.com/v1/pages"
_NOTION_HEADERS = {
    "Authorization": f"Bearer {NOTION_TOKEN}",
    "Content-Type": "application/json",
    "Notion-Version": "2022-06-28",
}


# ---------------------------------------------------------------------------
# Email
# ---------------------------------------------------------------------------

def _build_email(signals: list[dict]) -> MIMEMultipart:
    date_str = datetime.now().strftime("%Y-%m-%d")
    msg = MIMEMultipart("alternative")
    msg["Subject"] = f"Memecoin Buy Now Signals — {len(signals)} found ({date_str})"
    msg["From"] = GMAIL_SENDER
    msg["To"] = GMAIL_RECIPIENT

    plain_parts = []
    for s in signals:
        plain_parts.append(
            f"Token:       {s['token_name']} ({s['symbol']})\n"
            f"Chain:       {s['chain']}\n"
            f"Price:       ${s['price_usd']}\n"
            f"24h Change:  +{s['price_change_24h']}%\n"
            f"Volume 24h:  ${s['volume_24h_usd']:,.0f}\n"
            f"Liquidity:   ${s['liquidity_usd']:,.0f}\n"
            f"Vol/Liq:     {s['vol_liq_ratio']}\n"
            f"DexScreener: {s['dexscreener_url']}"
        )
    plain = "\n\n---\n\n".join(plain_parts)

    html_rows = "".join(
        f"<tr>"
        f"<td><b>{s['token_name']}</b> ({s['symbol']})</td>"
        f"<td>{s['chain']}</td>"
        f"<td>${s['price_usd']}</td>"
        f"<td style='color:green'>+{s['price_change_24h']}%</td>"
        f"<td>${s['volume_24h_usd']:,.0f}</td>"
        f"<td>${s['liquidity_usd']:,.0f}</td>"
        f"<td>{s['vol_liq_ratio']}</td>"
        f"<td><a href='{s['dexscreener_url']}'>View</a></td>"
        f"</tr>"
        for s in signals
    )
    html = f"""<html><body>
<h2>Memecoin Buy Now Signals — {date_str}</h2>
<table border="1" cellpadding="6" cellspacing="0" style="border-collapse:collapse;font-family:monospace">
  <tr style="background:#f0f0f0">
    <th>Token</th><th>Chain</th><th>Price</th><th>24h Change</th>
    <th>Volume 24h</th><th>Liquidity</th><th>Vol/Liq</th><th>Link</th>
  </tr>
  {html_rows}
</table>
<p style="color:#888;font-size:12px">Generated by memecoin-intel · {date_str}</p>
</body></html>"""

    msg.attach(MIMEText(plain, "plain"))
    msg.attach(MIMEText(html, "html"))
    return msg


def send_email(signals: list[dict]) -> bool:
    """Send a buy-now summary email. Returns True on success."""
    if not signals:
        return False
    msg = _build_email(signals)
    with smtplib.SMTP_SSL("smtp.gmail.com", 465) as server:
        server.login(GMAIL_SENDER, GMAIL_APP_PASSWORD)
        server.sendmail(GMAIL_SENDER, GMAIL_RECIPIENT, msg.as_string())
    log.info("Email sent to %s (%d signal(s))", GMAIL_RECIPIENT, len(signals))
    return True


