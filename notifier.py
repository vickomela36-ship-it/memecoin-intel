import logging
import smtplib
from datetime import datetime, timezone
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText

from notion_client import Client as NotionClient

from config import (
    ALERT_EMAIL,
    GMAIL_APP_PASSWORD,
    GMAIL_USER,
    NOTION_DATABASE_ID,
    NOTION_TOKEN,
)

logger = logging.getLogger(__name__)


def send_buy_alert_email(signals: list):
    """Send a single HTML email summarising all buy-now signals."""
    if not signals:
        return
    if not GMAIL_USER or not GMAIL_APP_PASSWORD:
        logger.warning("Gmail credentials not configured — skipping email alert.")
        return

    count = len(signals)
    subject = f"[Memecoin Intel] {count} BUY NOW Signal{'s' if count > 1 else ''} Detected"

    rows = ""
    for s in signals:
        change_color = "green" if s["price_change_24h"] >= 0 else "red"
        rows += (
            f"<tr>"
            f"<td style='padding:8px;border:1px solid #444;'>{s['coin']}</td>"
            f"<td style='padding:8px;border:1px solid #444;'>${s['price_usd']:.8f}</td>"
            f"<td style='padding:8px;border:1px solid #444;color:{change_color};'>{s['price_change_24h']:+.1f}%</td>"
            f"<td style='padding:8px;border:1px solid #444;'>${s['volume_24h']:,.0f}</td>"
            f"<td style='padding:8px;border:1px solid #444;'>${s['liquidity_usd']:,.0f}</td>"
            f"<td style='padding:8px;border:1px solid #444;'>{s['signal_strength']}</td>"
            f"<td style='padding:8px;border:1px solid #444;'><a href='{s['pair_url']}' style='color:#00b4d8;'>Chart</a></td>"
            f"</tr>"
        )

    now_str = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    body = f"""
<html>
<body style="font-family:Arial,sans-serif;background:#0d0d0d;color:#e0e0e0;padding:20px;">
  <h2 style="color:#e63946;">🚀 Memecoin BUY NOW Alert</h2>
  <p style="color:#aaa;">{count} signal(s) detected at {now_str}</p>
  <table style="border-collapse:collapse;width:100%;margin-top:12px;">
    <thead>
      <tr style="background:#1a1a2e;color:#fff;">
        <th style="padding:8px;border:1px solid #444;">Coin</th>
        <th style="padding:8px;border:1px solid #444;">Price (USD)</th>
        <th style="padding:8px;border:1px solid #444;">24h Change</th>
        <th style="padding:8px;border:1px solid #444;">24h Volume</th>
        <th style="padding:8px;border:1px solid #444;">Liquidity</th>
        <th style="padding:8px;border:1px solid #444;">Strength</th>
        <th style="padding:8px;border:1px solid #444;">Link</th>
      </tr>
    </thead>
    <tbody style="background:#111;">{rows}</tbody>
  </table>
  <p style="color:#555;font-size:11px;margin-top:16px;">
    Generated by memecoin-intel. Not financial advice.
  </p>
</body>
</html>"""

    msg = MIMEMultipart("alternative")
    msg["From"] = GMAIL_USER
    msg["To"] = ALERT_EMAIL
    msg["Subject"] = subject
    msg.attach(MIMEText(body, "html"))

    try:
        with smtplib.SMTP("smtp.gmail.com", 587) as server:
            server.ehlo()
            server.starttls()
            server.login(GMAIL_USER, GMAIL_APP_PASSWORD)
            server.send_message(msg)
        logger.info("Alert email sent to %s (%d signal(s))", ALERT_EMAIL, count)
    except Exception as exc:
        logger.error("Email send failed: %s", exc)


def log_signal_to_notion(signal: dict):
    """Append a single buy-now signal row to the Notion tracking database."""
    if not NOTION_TOKEN:
        logger.warning("NOTION_TOKEN not set — skipping Notion log.")
        return
    if not NOTION_DATABASE_ID:
        logger.warning("NOTION_DATABASE_ID not set — skipping Notion log.")
        return

    try:
        notion = NotionClient(auth=NOTION_TOKEN)
        notion.pages.create(
            parent={"database_id": NOTION_DATABASE_ID},
            properties={
                "Signal Name": {
                    "title": [{"text": {"content": signal["signal_name"]}}]
                },
                "Coin": {
                    "rich_text": [{"text": {"content": signal["coin"]}}]
                },
                "Price (USD)": {"number": signal["price_usd"]},
                "Price Change %": {"number": signal["price_change_24h"]},
                "Signal Strength": {"select": {"name": signal["signal_strength"]}},
                "Timestamp": {"date": {"start": signal["timestamp"]}},
                "Volume 24h": {"number": signal["volume_24h"]},
                "Notes": {
                    "rich_text": [{"text": {"content": signal.get("pair_url", "")}}]
                },
            },
        )
        logger.info("Logged to Notion: %s", signal["signal_name"])
    except Exception as exc:
        logger.error("Notion log failed: %s", exc)
